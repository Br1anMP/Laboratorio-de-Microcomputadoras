PROCESSOR 16f877
INCLUDE <p16f877.inc>
 
ORG 0
GOTO INICIO
ORG 5
 
INICIO:
MOVLW 0X20; W<- 0X20
MOVWF FSR ;(FSR) <- W
MOVF INDF,W
MOVWF 0X40
 
LOOP: 
INCF FSR, 1
MOVF INDF, W
SUBWF 0X40, W
BTFSS STATUS, C
GOTO MENOR
GOTO MAYORIGUAL

MAYORIGUAL: 
MOVF INDF, W
MOVWF 0X40
BTFSS FSR, 6
GOTO LOOP

GOTO $

MENOR: 
BTFSS FSR,6
GOTO LOOP
GOTO $

END