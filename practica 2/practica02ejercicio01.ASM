PROCESSOR 16f877
INCLUDE <p16f877.inc>
	ORG 0 ; vector de reset
	GOTO INICIO

	ORG 5
INICIO: 
	;BCF STATUS,RP1
	;BSF STATUS,RP0
	MOVLW 0X20; W<- 0X20
	MOVWF FSR ;(FSR) <- W
LOOP: 
	MOVLW 0X5F ; W<- 0X5F
	MOVWF INDF ; (INDF) <- W
	INCF FSR ; (FSR) <- (FSR) + 1
	BTFSS FSR,6 ; ¿YA ACABE?  ¿FSR.6 == 1?
	GOTO LOOP ; NO, VE A LOOP
	GOTO $  ; SI, YA TERMINE: ETIQUETA GOTO 
	
	END